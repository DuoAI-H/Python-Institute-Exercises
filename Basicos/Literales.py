print("2")
print(2) # Como 2 es un literal, lo puede imprimir directamente
         # Pues su valor es conocido, no necesita pertenecer a una variable
print(222_222) # Python permite el uso de guion bajo para separar visualmente los literales
               # Esto es solo para mejorar la legibilidad del numero
print(0o123) # Representacion Octal
print(0x123) # Representacion Hexadecimal
print(2.5) # Numeros flotantes

# Omicion de un digito en un numero flotante:

print(.4)
print(4.)

# Numero en notacion cientifica

print(2e-3)
print(0.8E4) # El exponente siempre debe ser entero